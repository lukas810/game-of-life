	public GamePanel(int length, int width) {
		this.length = length;
		this.width = width;
		this.addMouseListener(new MouseAdapter() {
			
			@Override
			public void mouseClicked(MouseEvent e) {
				int fieldX = e.getX() / 20;
				int fieldY = e.getY() / 20;

				playingField[fieldX][fieldY] = playingField[fieldX][fieldY] == 1 ? 0 : 1;

				repaint();

			}
		});
		this.setLayout(new GridBagLayout());
		this.add(getGenerationLabel(), new GridBagConstraints(0, 0, 1, 1, 1, 1, GridBagConstraints.NORTHWEST,
					GridBagConstraints.NONE, new Insets(5, 5, 5, 5), 0, 0));

		startUpdating();
	}

	@Override
	protected void paintComponent(Graphics g) {
		super.paintComponent(g);

		for (int i = 0; i <= width; i += 20) {
			g.drawLine(0, i, length, i);
		}

		for (int i = 0; i <= length; i += 20) {
			g.drawLine(i, 0, i, width);
		}

		for (int i = 0; i < playingField.length; i++) {
			for (int j = 0; j < playingField[i].length; j++) {
				if (playingField[i][j] == 1) {
					g.fillRect(i * 20, j * 20, 20, 20);
				}

			}
		}
	}

	private void startUpdating() {
		Timer timer = new Timer(1000, e -> {
			if (running) {
				repaint();
				calculateNewPlayingField();
				generationLabel.setText("Generation: " + ++generation);
			}
		});
		timer.start();
	}

